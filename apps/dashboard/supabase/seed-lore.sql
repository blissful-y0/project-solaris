## 능력 유형별 특성
### 동조형 (Synchronized)
안정적이고 정밀하며 출력 조절이 자유롭고 지속 시간이 길다. 헬리오스의 연산 지원을 실시간으로 받기 때문에 능력 사용 중 최적의 타이밍과 궤도를 직감으로 파악할 수 있다.

- 대가: 없는 것처럼 보인다. 그게 함정이다. 능력을 쓸수록 헬리오스와의 동조가 깊어지고 자아의 경계가 희미해진다. 고참 수호자들이 감정 표현이 점점 기계적으로 변하는 건 이 때문이다.
- 전투 스타일: 군사적, 체계적. 팀 단위 연계가 뛰어나고 전술적 판단이 빠르다.

### 비동조형 (Asynchronized)
거칠고 폭발적이다. 순간 출력은 동조형을 압도하지만 정밀 제어가 어렵고 지속 시간이 짧다. 감정 상태에 출력이 직결되어 분노하면 폭주하고 공포에 빠지면 능력이 소멸한다.

- 대가: 명확하다. 사용 후 육체적 반동이 온다. 코피, 근육 경련, 의식 혼탁, 심하면 일시적 감각 상실. 과도한 사용은 수명을 깎는다.
- 전투 스타일: 게릴라적, 즉흥적. 개인 화력에 의존하며 단기 결전을 선호한다. 장기전은 불리하다.

### 전향자
원래 동조형이었으나 헬리오스에게서 이탈한 자. 도시 안에서는 기존 동조형 능력을 유지하지만 헬리오스의 연산 지원이 끊겨 정밀도와 지속력이 하락한다. 능력 패턴이 보안국 데이터베이스에 등록되어 있어 사용 즉시 감지된다. 도시 밖에서는 동조형의 안정성과 비동조형의 폭발력 사이 어딘가에 위치한다.

