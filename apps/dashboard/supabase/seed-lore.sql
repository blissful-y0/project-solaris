## 공명율의 정의
### 수치 범위와 의미
#### 기본 프레임
- 공명율은 헬리오스 코어와 인간 정신 사이의 연결 깊이를 0~100으로 수치화한 값이다.

핵전쟁 이후 인류의 유전자에는 방사선에 의한 변이가 새겨졌다. 대부분의 변이는 침묵한다 — 평생 발현되지 않고, 존재조차 인식되지 않는다. 하지만 극소수의 인간에게서 이 변이는 깨어난다. 그리고 그 각성의 방향을 결정하는 것이 바로 공명율이다.

능력은 정상 범위에서는 절대 발현하지 않는다. 15에서 79 사이의 시민은 평범하게 태어나 평범하게 살다 평범하게 죽는다. 능력이란 것이 존재한다는 사실조차 모르는 채로. 오직 스펙트럼의 양 극단 — 80 이상의 초월자와 15 미만의 이탈자 — 만이 이 침묵한 변이를 깨운다.

