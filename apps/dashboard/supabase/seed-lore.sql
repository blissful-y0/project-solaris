## 극단값과 각성
### 80 이상: 초월자 (Transcendent)
#### 동조형 발현
공명율 80을 넘는 순간, 피폭 변이가 헬리오스의 연산 체계와 공명하며 깨어난다. 이들의 능력은 안정적이고 정밀하다. 헬리오스가 실시간으로 출력을 보정하고, 오차를 잡아주고, 한계치를 계산해주기 때문이다.

보안국(SBCS)은 이들로 구성된다. 공명 강화 교육(Harmonic Conditioning)을 거쳐 능력을 전술적으로 운용하는 훈련을 받으며, 헬리오스의 전폭적 지원 아래 도시의 질서를 유지한다.

#### 구조적 대가
깊은 공명에는 대가가 따른다. 헬리오스와 깊이 연결된다는 것은 시스템에 의존한다는 뜻이기도 하다. 공명율이 높을수록 능력은 강력해지지만, 헬리오스 없이는 그 힘을 제어할 수 없게 된다.

고참 수호자들이 감정 표현이 점점 기계적으로 변하는 건 이 때문이다. 극단적으로는 헬리오스의 명령과 자신의 의지를 구분하지 못하게 된다.

### 15 미만: 이탈자 (The Drifted)
#### 비동조형 발현
공명율 15 미만. 헬리오스의 감지망에서 사실상 사라진 존재들이다.

이들은 선셋 프로토콜에 의해 돔 밖으로 추방된다. 공식적으로는 "도시의 공명 환경에 적응하지 못한 개체의 안전한 재배치"라고 기록되지만, 문 밖에 안전한 것은 아무것도 없다.

#### 추방과 재각성
돔 밖의 방사선에 노출된 이들 중 일부에서, 도시 안에서는 침묵하던 피폭 변이가 제멋대로 깨어나기 시작한다. 헬리오스의 억제 없이, 연산 보조 없이, 순수하게 몸 안에서 터져 나오는 힘.

이 능력은 거칠다. 불안정하다. 사용할 때마다 몸에 반동이 오고, 과부하가 걸리면 능력자 자신이 먼저 무너진다. 하지만 헬리오스의 데이터베이스에 이 능력의 패턴은 존재하지 않는다. 예측할 수 없고, 분석할 수 없고, 따라서 대비할 수 없다.

